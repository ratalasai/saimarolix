package loops;

public class DoWhile_loop {

	public static void main(String[] args) {


		int i=7;
		
		do {
			System.out.print(i+" ");
			i++;
		} 
		while (i<=12);   //satisfy condition;
		
		
		int s=10;
		
		do {
			System.out.print(s);
			s++;
		}
		while (s<=5);  //not satisfy condition;
		
	}

}
